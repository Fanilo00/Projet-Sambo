public class Categorie_Bateau
{

    int Id_categorie;
    String Categorie;
    int Prix_Categorie;

    
    public int getId_categorie() {
        return Id_categorie;
    }
    public void setId_categorie(int id_categorie) {
        Id_categorie = id_categorie;
    }
    public String getCategorie() {
        return Categorie;
    }
    public void setCategorie(String categorie) {
        Categorie = categorie;
    }
    public int getPrix_Categorie() {
        return Prix_Categorie;
    }
    public void setPrix_Categorie(int prix_Categorie) {
        Prix_Categorie = prix_Categorie;
    }
    

}